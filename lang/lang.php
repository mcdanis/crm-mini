class LanguageManager
{
    private static $activeLang = 'id';
    private static $languages = [];

    public static function init($lang_id, $lang_en)
    {
        self::$languages = [
            'id' => $lang_id,
            'en' => $lang_en,
        ];
    }

    public static function setLanguage($lang)
    {
        if (array_key_exists($lang, self::$languages)) {
            self::$activeLang = $lang;
            return true;
        }
        return false;
    }

    public static function getLanguage()
    {
        return self::$activeLang;
    }

    public static function translate($key)
    {
        if (isset(self::$languages[self::$activeLang][$key])) {
            return self::$languages[self::$activeLang][$key];
        }

        // Fallback ke English jika tidak ada di bahasa aktif
        if (self::$activeLang !== 'en' && isset(self::$languages['en'][$key])) {
            return self::$languages['en'][$key];
        }

        // Return key jika tidak ditemukan
        return $key;
    }
}

// Initialize the language manager
LanguageManager::init($lang_id, $lang_en);

// Helper function untuk kemudahan penggunaan
function t($key)
{
    return LanguageManager::translate($key);
}

// Helper function untuk mengatur bahasa
function setLang($lang)
{
    return LanguageManager::setLanguage($lang);
}

// Helper function untuk mendapatkan bahasa aktif
function getLang()
{
    return LanguageManager::getLanguage();
}

